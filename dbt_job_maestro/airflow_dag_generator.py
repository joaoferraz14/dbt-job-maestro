"""Generate Airflow DAG Python files from dbt selectors."""

import json
import os
from datetime import datetime
from typing import Any, Dict, List, Optional, Set

from dbt_job_maestro.config import AirflowConfig


class AirflowDAGGenerator:
    """Generate Airflow DAG Python files from dbt selectors.

    Mirrors the JobGenerator workflow but targets Airflow instead of dbt Cloud.
    Each selector becomes a BashOperator task that runs `dbt build --selector <name>`.
    Seeds and snapshots selectors get their correct dbt verbs (seed / snapshot).
    """

    def __init__(
        self,
        config: AirflowConfig,
        manifest_data: Optional[Dict[str, Any]] = None,
    ):
        self.config = config
        self.manifest_data = manifest_data

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def generate_dag(self, selectors: List[Dict[str, Any]]) -> str:
        """Generate a Python DAG file as a string.

        Args:
            selectors: Selector definitions loaded from selectors.yml.

        Returns:
            Python source code string for an Airflow DAG.
        """
        filtered = self._filter_selectors(selectors)
        deps = self._resolve_dependencies(filtered)
        return self._render_dag(filtered, deps)

    def write_dag(self, dag_code: str, output_path: str) -> None:
        """Write generated DAG code to a .py file.

        Args:
            dag_code: Python source string from generate_dag().
            output_path: Destination file path.
        """
        os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
        with open(output_path, "w") as f:
            f.write(dag_code)

    def load_manifest(self, manifest_path: str) -> Optional[Dict[str, Any]]:
        """Load dbt manifest.json.

        Args:
            manifest_path: Path to manifest.json generated by dbt compile.

        Returns:
            Parsed manifest dict, or None if the file is missing.
        """
        if not os.path.exists(manifest_path):
            return None
        with open(manifest_path) as f:
            return json.load(f)

    # ------------------------------------------------------------------
    # Selector filtering
    # ------------------------------------------------------------------

    def _filter_selectors(self, selectors: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        """Drop freshness selectors - they don't map to dbt run/build steps."""
        return [
            s
            for s in selectors
            if not s["name"].startswith("freshness_")
            and not s["name"].startswith("automatically_generated_freshness_")
        ]

    # ------------------------------------------------------------------
    # Selector type & dbt command helpers
    # ------------------------------------------------------------------

    def _get_selector_type(self, selector_name: str) -> str:
        """Classify a selector as 'seeds', 'snapshots', 'full_refresh', or 'models'."""
        prefix = self.config.selector_prefix
        if selector_name == f"{prefix}_seeds" or selector_name.endswith("_seeds"):
            return "seeds"
        if selector_name == f"{prefix}_snapshots" or selector_name.endswith("_snapshots"):
            return "snapshots"
        if selector_name.endswith("_full_refresh_incremental"):
            return "full_refresh"
        return "models"

    def _get_dbt_command(self, selector: Dict[str, Any]) -> str:
        """Build the dbt CLI invocation for a selector.

        Args:
            selector: Selector definition dict with at minimum a 'name' key.

        Returns:
            dbt CLI command string (e.g. "dbt build --selector maestro_staging ...").
        """
        name = selector["name"]
        sel_type = self._get_selector_type(name)

        if sel_type == "seeds":
            parts = ["dbt", "seed"]
        elif sel_type == "snapshots":
            parts = ["dbt", "snapshot"]
        elif sel_type == "full_refresh":
            parts = ["dbt", "build", "--full-refresh"]
        else:
            parts = ["dbt", "build"]

        parts += ["--selector", name]

        if self.config.dbt_project_dir:
            parts += ["--project-dir", self.config.dbt_project_dir]
        if self.config.dbt_profiles_dir:
            parts += ["--profiles-dir", self.config.dbt_profiles_dir]
        if self.config.dbt_target:
            parts += ["--target", self.config.dbt_target]
        if self.config.dbt_threads:
            parts += ["--threads", str(self.config.dbt_threads)]

        return " ".join(parts)

    # ------------------------------------------------------------------
    # Model extraction from selector definitions
    # ------------------------------------------------------------------

    def _extract_fqn_models(self, selector: Dict[str, Any]) -> Set[str]:
        """Recursively extract model/seed/snapshot names from FQN selector entries."""
        names: Set[str] = set()
        definition = selector.get("definition", {})

        def _walk(node: Any) -> None:
            if isinstance(node, dict):
                if node.get("method") == "fqn" and "value" in node:
                    names.add(node["value"])
                for v in node.values():
                    _walk(v)
            elif isinstance(node, list):
                for item in node:
                    _walk(item)

        _walk(definition)
        return names

    # ------------------------------------------------------------------
    # Dependency resolution
    # ------------------------------------------------------------------

    def _resolve_dependencies(self, selectors: List[Dict[str, Any]]) -> Dict[str, Set[str]]:
        """Compute {selector_name: set_of_upstream_selector_names}.

        Three modes are supported:
        - parallel:    no dependencies - all tasks run at the same time.
        - sequential:  each task depends on the previous one in list order.
        - dependency:  type-based ordering (seeds → snapshots → models) plus
                       optional manifest-derived cross-selector graph deps.
        """
        deps: Dict[str, Set[str]] = {s["name"]: set() for s in selectors}

        if self.config.orchestration_mode == "parallel":
            return deps

        if self.config.orchestration_mode == "sequential":
            for i in range(1, len(selectors)):
                deps[selectors[i]["name"]].add(selectors[i - 1]["name"])
            return deps

        # dependency mode
        return self._resolve_dependency_mode(selectors, deps)

    def _resolve_dependency_mode(
        self,
        selectors: List[Dict[str, Any]],
        deps: Dict[str, Set[str]],
    ) -> Dict[str, Set[str]]:
        """Add type-based and manifest-derived dependencies."""
        type_rank = {"seeds": 0, "snapshots": 1, "full_refresh": 2, "models": 3}
        sel_types = {s["name"]: self._get_selector_type(s["name"]) for s in selectors}

        # Every selector depends on all selectors with a lower type rank
        for sel in selectors:
            name = sel["name"]
            my_rank = type_rank[sel_types[name]]
            for other in selectors:
                other_name = other["name"]
                if other_name == name:
                    continue
                if type_rank[sel_types[other_name]] < my_rank:
                    deps[name].add(other_name)

        # Supplement with actual manifest node dependencies when available
        if self.manifest_data:
            self._add_manifest_deps(selectors, deps)

        return deps

    def _add_manifest_deps(
        self,
        selectors: List[Dict[str, Any]],
        deps: Dict[str, Set[str]],
    ) -> None:
        """Add cross-selector dependencies derived from the dbt manifest graph.

        For every model node in the manifest, if its upstream dependency belongs
        to a *different* selector, add that selector as an upstream.
        """
        # node short-name → selector that owns it
        node_to_selector: Dict[str, str] = {}
        for sel in selectors:
            for model_name in self._extract_fqn_models(sel):
                node_to_selector[model_name] = sel["name"]

        nodes = self.manifest_data.get("nodes", {})
        for node_key, node in nodes.items():
            resource_type = node.get("resource_type", "")
            if resource_type not in ("model", "seed", "snapshot"):
                continue
            node_name = node.get("name", "")
            if node_name not in node_to_selector:
                continue
            my_sel = node_to_selector[node_name]

            for dep_key in node.get("depends_on", {}).get("nodes", []):
                # dep_key format: "model.project_name.node_name"
                dep_parts = dep_key.split(".")
                if len(dep_parts) < 3:
                    continue
                dep_name = dep_parts[-1]
                if dep_name not in node_to_selector:
                    continue
                dep_sel = node_to_selector[dep_name]
                if dep_sel != my_sel:
                    deps[my_sel].add(dep_sel)

    # ------------------------------------------------------------------
    # Code rendering
    # ------------------------------------------------------------------

    def _selector_to_task_var(self, selector_name: str) -> str:
        """Convert a selector name to a Python variable name for its Airflow task."""
        return f"run_{selector_name}"

    def _render_dag(
        self,
        selectors: List[Dict[str, Any]],
        deps: Dict[str, Set[str]],
    ) -> str:
        """Render the complete Python DAG source file."""
        lines: List[str] = []

        # --- File header ---
        lines += [
            '"""',
            "Auto-generated by dbt-job-maestro.",
            "DO NOT EDIT - regenerate with: maestro generate-dags",
            '"""',
            "",
            "from datetime import datetime, timedelta",
            "",
            "from airflow import DAG",
            "from airflow.operators.bash import BashOperator",
            "",
            "",
        ]

        # --- default_args ---
        lines += [
            "default_args = {",
            f'    "owner": "{self.config.owner}",',
            '    "depends_on_past": False,',
            '    "email_on_failure": False,',
            f'    "retries": {self.config.retries},',
            f'    "retry_delay": timedelta(minutes={self.config.retry_delay_minutes}),',
            "}",
            "",
            "",
        ]

        # --- DAG context ---
        try:
            dt = datetime.strptime(self.config.start_date, "%Y-%m-%d")
            start_date_expr = f"datetime({dt.year}, {dt.month}, {dt.day})"
        except ValueError:
            start_date_expr = "datetime(2024, 1, 1)"

        lines += [
            "with DAG(",
            f'    dag_id="{self.config.dag_id}",',
            "    default_args=default_args,",
            f'    schedule_interval="{self.config.schedule_interval}",',
            f"    start_date={start_date_expr},",
            "    catchup=False,",
            f"    tags={repr(self.config.tags)},",
            ") as dag:",
            "",
        ]

        # --- Tasks ---
        task_vars: Dict[str, str] = {}
        for sel in selectors:
            name = sel["name"]
            task_var = self._selector_to_task_var(name)
            task_vars[name] = task_var
            cmd = self._get_dbt_command(sel)
            lines += [
                f"    {task_var} = BashOperator(",
                f'        task_id="{task_var}",',
                f'        bash_command="{cmd}",',
                "    )",
                "",
            ]

        # --- Dependency lines ---
        dep_lines = self._render_dependency_lines(selectors, deps, task_vars)
        if dep_lines:
            lines.append("    # Task dependencies")
            lines.extend(dep_lines)
            lines.append("")

        return "\n".join(lines) + "\n"

    def _render_dependency_lines(
        self,
        selectors: List[Dict[str, Any]],
        deps: Dict[str, Set[str]],
        task_vars: Dict[str, str],
    ) -> List[str]:
        """Render Airflow `>>` operator lines for task dependencies."""
        lines: List[str] = []
        for sel in selectors:
            name = sel["name"]
            upstreams = sorted(u for u in deps.get(name, set()) if u in task_vars)
            if not upstreams:
                continue
            downstream_var = task_vars[name]
            if len(upstreams) == 1:
                lines.append(f"    {task_vars[upstreams[0]]} >> {downstream_var}")
            else:
                upstream_expr = ", ".join(task_vars[u] for u in upstreams)
                lines.append(f"    [{upstream_expr}] >> {downstream_var}")
        return lines
